let activePoster = 1;
$("#prev-poster").click(() => {
    activePoster = Math.max(1, activePoster - 1);
    if (activePoster == 1) $("#prev-poster").hide();
    $("#next-poster").show()
    $("#poster-img-2").attr("src", `./Images/projects/${activePoster+1}.png`)
    $("#poster-img-1").attr("src", `./Images/projects/${activePoster}.png`)
})
